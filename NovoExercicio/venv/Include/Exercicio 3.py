# 3 - Elaborar um algoritmo para ler dois números inteiros e imprimir a sequinte saida : dividendo, dividir, quociente e resto.

num1 = float(input(" Entre com primeiro número : "))
num2 = float(input(" Entre com segundo número : "))
dividendo = 2 *num1,num2
dividor = 3 *num1,num2
quociente = 4 *num1,num2
resto = 5 *num1,num2
print(" O dividendo de {},{} é {}.".format(num1,dividendo,num2))
print(" O dividor de {},{} é {}.".format(num1,dividor,num2))
print(" O quociente de {},{} é {}.".format(num1,quociente,num2))
print(" O resto de {},{} é {}.".format(num1,resto,num2))