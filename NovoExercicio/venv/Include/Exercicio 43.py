# Crie um programa que leia uma frase qualquer e diga se ela é um polindromo, desconsidereramos os espaços. Exemplos APOS á SOPA
frase = str(input(" Digite uma frase : ")).__str__().upper()
palavras = frase.split()
juntos = " ".join(palavras)
inverso = " "
for  frase  in range (len/juntos (-1,-1,-1)) :
    inverso += juntos
    print(" Temos um palindromo ! ")
else :
    print(" A frase digitado não é um polindromo não é um polindromo ! ")