# 6 - Elaborar um algoritmo para ler um numero inteiro de 3 casas decimais e imprimir a soma dos algoritmo da casa das dezenas e das unidades.

from decimal import Decimal
c1 = float(input(" Informar a primeira casa : "))
c1 = decimal(" 200 ")*5
print(c1)

c2 = float(input(" Informar a segunda casa : "))
